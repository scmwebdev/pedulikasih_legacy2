<h1>ARE YOU THE REAL SINGER?</h1>
<br /><br />
<div style="text-align:justify;text-justify:inter-word;">
<p>Setelah sukses di lebih dari 10 negara di dunia, kini program THE VOICE hadir di Indonesia. Di program ini, kemampuan olah vokal sangat diutamakan. Dengan bimbingan 4 Coach di THE VOICE INDONESIA bakal bikin kamu menjadi The Real Singer!
Because we looking for The Real Singer!!</p>
<p>Kalau kamu punya kemampuan itu, daftarkan dirimu dan jadilah bagian dari The Voice Indonesia! Singing competition tersukses dan paling beda di dunia.</p>
<br />
<h3>PERSYARATAN UMUM PENDAFTARAN THE VOICE INDONESIA</h3>
<ol>
<li>Mempunyai kemampuan istimewa di bidang tarik suara (Real Singer)</li>
<li>Usia minimal 17 tahun dan maksimal 45 tahun</li>
<li>Kategori penampilan adalah bernyanyi secara Solo atau Duet</li>
<li>Mampu mengikuti audisi dan mematuhi seluruh peraturan audisi yang sudah ditetapkan oleh The Voice Indonesia</li>
</ol>
<br />
<h3>PERSYARATAN AUDISI ONLINE THE VOICE INDONESIA</h3>
<ol>
<li>Durasi video maksimal 2 menit.</li>
<li>Pilih lagu yang sesuai dengan karakter Anda.</li>
<li>Video berupa rekaman vokal, TANPA instrumen musik.</li>
<li>Video yang diunggah harus baru dibuat (maksimal dibuat pada Oktober 2012).</li>
<li>Periode online audition adalah : 1 November – 1 Desember 2012</li>
<li>Ingat, Audisi ini bisa mengubah hidup Anda, jadi tampillah semaksimal mungkin!</li>
</ol>
</div>
