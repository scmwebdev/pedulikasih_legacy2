<h1>FAQ Galaxy Superstar</h1>
<br /><br />
<ol>
	<li>
		Q  : Kalau daftar melalui website gimana cara ikutan audisinya ?<br />
		A  : Pihak panitia akan memilah-milah peserta sesuai dengan kota audisi dan yang lolos berdasarkan kriteria akan dihubungi untuk ikut audisi serta menentukan tanggal, tempat, dan kota audisi<br /><br />
	</li>
	<li>
		Q  : Bagaimana mendapatkan formulir pendaftaran Galaxy Superstar kalau mendaftar tidak menggunakan website<br />
		A  :  Formulir bisa didapatkan di Tabloid Gaul dan Tabloid Bintang atau dapat diambil di Radio partner dikotamu (Trax FM Jkt, Kiss Medan, Prambors Mkss, Istara FM Sby, Gajahmada FM Smg)<br /><br />
	</li>
	<li>
		Q  : Apa  saja persyaratan yang harus dipenuhi untuk ikutan audisi Galaxy Superstar<br />
		A  : Usia 17 tahun ke atas, mengisi formulir pendaftaran, sertakan pas photo  1 badan dan close up ukuran 4 R, sertakan  demo nyani/dance dalam bentuk DVD/CD (kalau ada), dan datang langsung ke tempat audisi<br /><br />
	</li>
	<li>
		Q  : Apa saja kriteria yang akan diaudisi ?<br />
		A   : Kriteria pertama yang akan diaudisi adalah menyanyi baik solo, duet maupun grup (acapella, vokal dll)  dengan membawakan lagu bebas sesuai yang dikuasai 
        Kriteria ke dua adalah menari/dance perorangan maupun grup dengan jenis tarian bebas (tradisional, modern,kontemporer)<br /><br />
	</li>
	<li>
		Q  : Di kota manakah pelaksanaan audisi diselenggarakan dan tanggal berapa saja ?<br />
		A  : Audisi dilaksanakan di 4 (empat) Kota  Medan (28 s/d 29 Januari 2012), Makssar (4 s/d 5 Februari 2012), Surabaya (11 s/d 12 Februari 2012), Semarang (18 s/d 19 Februari 2012)<br />
	  <br />
	</li>
	<li>
		Q  : Jika lolos ke Korea dilatih apa saja dan berapa lama waktu pelatihanya ?<br />
		A : Buat peserta yang lolos akan di latih beberapa materi seperti olah vokal,  dancing, acting, performing, stage act, hingga menjadi bintang Asia, kurang lebih selama 6 (enam) bulan.<br /><br />
	</li>
</ol>