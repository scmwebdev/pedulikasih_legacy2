<h1>Munculnya I-Pop untuk menyaingi K-Pop</h1>
<br /><br />
<p>Galaxy Superstar akan menjadi New I-Pop Star untuk mengguncang Asia.  
Sekarang saatnya masyarakat Indonesia bisa memilih langsung artis yang mereka sukai. Program ini sudah dirancang 
selama 1 tahun dan dirancang dengan ahli K-Pop termasuk musisi dan Producer terbaik dari Korea. 
Rencananya proses seleksi akan dilakukan di 5 kota di Indonesia, yaitu :</p>

<ul>
	<li><b>Jakarta</b>	: 17 s/d 19 Jan 2012</li>
	<li><b>Medan</b>	: 28 s/d 29 Januari 2012</li>
	<li><b>Makassar</b>	: 4 s/d 5 Februari 2012</li>
	<li><b>Surabaya</b>	: 11 s/d 12 Februari 2012</li>
	<li><b>Semarang</b>	: 18 s/d 19 Februari 2012</li>
</ul>

<p>Pemenang diakhir audisi berhak mendapat banyak keuntungan. Pemenang akan mendapat hadiah untuk 
terbang ke Korea dan mendapatkan pelatihan vokal dan training khusus dari para musisi yang telah 
melahirkan banyak bintang K-Pop. Dua di antaranya yakni :<p>
